const ARABIC_DIGITS = "٠١٢٣٤٥٦٧٨٩";
const PERSIAN_DIGITS = "۰۱۲۳۴۵۶۷۸۹";

function toWesternDigits(value: string) {
  return value
    .replace(/[٠-٩]/g, (digit) => String(ARABIC_DIGITS.indexOf(digit)))
    .replace(/[۰-۹]/g, (digit) => String(PERSIAN_DIGITS.indexOf(digit)));
}

function normalize(value: string) {
  return value.replace(/\s+/g, " ").trim();
}

export function translateText(value: string, locale: Locale): string {
  if (locale === "ar" || !value) return value;

  const leading = value.match(/^\s*/)?.[0] ?? "";
  const trailing = value.match(/\s*$/)?.[0] ?? "";
  const core = normalize(value);
  if (!core) return value;

  let translated = AR_TO_EN[core];
  if (!translated) {
    translated = core;
    for (const [pattern, replacement] of DYNAMIC_REPLACEMENTS) {
      translated = translated.replace(pattern, replacement);
    }
  }

  translated = toWesternDigits(translated).replace(/،/g, ",");
  return `${leading}${translated}${trailing}`;
}

